<?php

use App\Models\User;

test('guests are redirected to the login page', function () {
    $response = $this->get('/events');
    $response->assertRedirect(route('login'));
});

test('authenticated users can visit events', function () {
    $user = User::factory()->create();
    $this->actingAs($user);

    $response = $this->get('/events');
    $response->assertOk();
});